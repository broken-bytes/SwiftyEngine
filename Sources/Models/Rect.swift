public struct Rect {

    public var x: Int32
    public var y: Int32
    public var width: UInt32
    public var height: UInt32

    public init(x: Int32, y: Int32, width: UInt32, height: UInt32) {
        self.x = x
        self.y = y
        self.width = width
        self.height = height
    }
}